      <h2>My different styles</h2>
      </p>
      <p>"Having a singular body of work is pass&eacute;, it's&nbsp;more important to be an interesting artist." Jason Middlebrook</p>
      <p>&nbsp;As a painter I have explored many different artistic domains and I follow the roads that my talent selects for me. Over the years it has become clear that my different styles lie in a spectrum of possibilities, varying from pure realism, via pure symbolism and surrealism to almost pure abstraction.</p>
      <p>The styles are for me associated with different modes of being. I travel from one style to another as my moods guide me. It is like living in different houses. There is a house of 'business as usual' and there is a house of dreams, there is a house of silence, a place to meditate and there is a house of pure line and color,&nbsp;where I dwell in state of Dionysian euphoria. Sometimes I live in one house for a couple of years, sometimes I shift from day to day, but they are all equally dear to me.</p>
      <p><img src="assets/img/collage.png" alt="" width="500" height="455" /></p>
      <p>&nbsp;The picture plane above gives an idea of this pictorial space. I got the idea from&nbsp;<a href="http://scottmccloud.com/4-inventions/triangle/">Scott McCloud</a>.&nbsp;My colleague at the university of Amsterdam, Dick Swaab, thinks the styles have to do with&nbsp;<a href="http://www.ideabooks.nl/9789491444135-dick-swaab-art-is-beauty-in-the-brain-of-the-beholder">different modes of the brain</a>, but that is more relevant for science than for art. It is what it is. Enjoy...&nbsp;</p>

